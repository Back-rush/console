<script lang="ts" context="module">
    export let showCreateTeam = writable(false);
</script>

<script lang="ts">
    import { page } from '$app/stores';
    import { Button } from '$lib/elements/forms';
    import {
        Empty,
        EmptySearch,
        AvatarInitials,
        SearchQuery,
        PaginationWithLimit
    } from '$lib/components';
    import Create from '../createTeam.svelte';
    import { goto } from '$app/navigation';
    import { toLocaleDateTime } from '$lib/helpers/date';
    import { Container, ContainerHeader } from '$lib/layout';
    import { base } from '$app/paths';
    import type { Models } from '@appwrite.io/console';
    import type { PageData } from './$types';
    import { writable } from 'svelte/store';
    import { readOnly } from '$lib/stores/billing';
    import { isCloud } from '$lib/system';
    import { canWriteTeams } from '$lib/stores/roles';
    import { Tooltip, Layout, Table } from '@appwrite.io/pink-svelte';

    export let data: PageData;

    const project = $page.params.project;
    const teamCreated = async (event: CustomEvent<Models.Team<Record<string, unknown>>>) => {
        await goto(`${base}/project-${project}/auth/teams/team-${event.detail.$id}`);
    };
</script>

<Container>
    <Layout.Stack direction="row" justifyContent="space-between">
        <Layout.Stack direction="row" alignItems="center">
            <SearchQuery search={data.search} placeholder="Search by name" />
        </Layout.Stack>
        <Layout.Stack direction="row" alignItems="center" justifyContent="flex-end">
            <Button on:mousedown={() => ($showCreateTeam = true)} event="create_user" size="s">
                <span class="icon-plus" aria-hidden="true" />
                <span class="text">Create team</span>
            </Button>
        </Layout.Stack>
    </Layout.Stack>

    {#if data.teams.total}
        <Table.Root>
            <svelte:fragment slot="header">
                <Table.Header.Cell>Name</Table.Header.Cell>
                <Table.Header.Cell>Members</Table.Header.Cell>
                <Table.Header.Cell>Created</Table.Header.Cell>
            </svelte:fragment>
            {#each data.teams.teams as team}
                <Table.Link href={`${base}/project-${project}/auth/teams/team-${team.$id}`}>
                    <Table.Cell>
                        <Layout.Stack direction="row" alignItems="center">
                            <AvatarInitials size={32} name={team.name} />
                            <span class="u-trim">{team.name}</span>
                        </Layout.Stack>
                    </Table.Cell>
                    <Table.Cell>
                        {team.total} members
                    </Table.Cell>
                    <Table.Cell>
                        {toLocaleDateTime(team.$createdAt)}
                    </Table.Cell>
                </Table.Link>
            {/each}
        </Table.Root>

        <PaginationWithLimit
            name="Teams"
            limit={data.limit}
            offset={data.offset}
            total={data.teams.total} />
    {:else if data.search}
        <EmptySearch target="teams" search={data.search} hidePagination={data.teams.total === 0}>
            <Button secondary size="s" href={`${base}/project-${$page.params.project}/auth/teams`}>
                Clear Search
            </Button>
        </EmptySearch>
    {:else}
        <Empty
            single
            allowCreate={$canWriteTeams}
            on:click={() => ($showCreateTeam = true)}
            href="https://appwrite.io/docs/references/cloud/client-web/teams"
            target="team" />
    {/if}
</Container>

<Create bind:showCreate={$showCreateTeam} on:created={teamCreated} />
